import { z } from 'zod';

export const userEditSchema = z.object({
  name: z
    .string()
    .min(2, 'Имя должно содержать минимум 2 символа')
    .max(64, 'Имя не может превышать 64 символа')
    .regex(/^[a-zA-Zа-яА-Я\s]+$/, 'Имя может содержать только буквы'),

  username: z
    .string()
    .min(2, 'Никнейм должен содержать минимум 2 символа')
    .max(64, 'Никнейм не может превышать 64 символа')
    .regex(
      /^[a-zA-Z0-9_]+$/,
      'Никнейм может содержать только буквы, цифры и _'
    ),

  email: z
    .string()
    .min(1, 'Email обязателен')
    .email('Некорректный формат email'),

  city: z
    .string()
    .min(2, 'Город должен содержать минимум 2 символа')
    .max(64, 'Город не может превышать 64 символа')
    .regex(
      /^[a-zA-Zа-яА-Я\s-]+$/,
      'Город может содержать только буквы и дефис'
    ),

  phone: z
    .string()
    .regex(/^\d+$/, 'Телефон должен содержать только цифры')
    .min(10, 'Телефон должен содержать минимум 10 цифр')
    .max(15, 'Телефон не может превышать 15 цифр'),

  companyName: z
    .string()
    .min(2, 'Название компании должно содержать минимум 2 символа')
    .max(64, 'Название компании не может превышать 64 символа'),
});

export type UserEditFormData = z.infer<typeof userEditSchema>;
